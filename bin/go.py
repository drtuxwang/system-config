#!/usr/bin/env python3
"""
GO compiler wrapper (sets GOROOT automatically)
"""

import os
import signal
import sys
from pathlib import Path

from command_mod import Command
from subtask_mod import Exec


class Main:
    """
    Main class
    """

    def __init__(self) -> None:
        try:
            self.config()
            sys.exit(self.run())
        except (EOFError, KeyboardInterrupt):
            sys.exit(114)
        except SystemExit as exception:
            sys.exit(exception)  # type: ignore

    @staticmethod
    def config() -> None:
        """
        Configure program
        """
        if hasattr(signal, 'SIGPIPE'):
            signal.signal(signal.SIGPIPE, signal.SIG_DFL)
        if os.linesep != '\n':
            def _open(file, *args, **kwargs):  # type: ignore
                if 'newline' not in kwargs and args and 'b' not in args[0]:
                    kwargs['newline'] = '\n'
                return open(str(file), *args, **kwargs)
            Path.open = _open  # type: ignore

    @staticmethod
    def run() -> int:
        """
        Start program
        """
        golang = Command('bin/go', errors='stop')
        golang.extend_args(sys.argv[1:])

        goroot = Path(golang.get_file()).parents[1]
        if Path(goroot, 'pkg').is_dir():
            os.environ['GOROOT'] = str(goroot)
        # Re-direct "$HOME/go/pkg/mod" cache
        os.environ['GOMODCACHE'] = str(Path(Path.home(), '.cache/go/pkg/mod'))

        Exec(golang.get_cmdline()).run()

        return 0


if __name__ == '__main__':
    if '--pydoc' in sys.argv:
        help(__name__)
    else:
        Main()
